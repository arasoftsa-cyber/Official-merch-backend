const getTemplateCatalogEntry = (templateKey) =>
  EMAIL_TEMPLATE_CATALOG[String(templateKey || "").trim().toLowerCase()] || null;

const listTemplateKeysByTier = (tier) => {
  const normalizedTier = String(tier || "").trim().toLowerCase();
  return Object.keys(EMAIL_TEMPLATE_CATALOG).filter((key) => {
    return EMAIL_TEMPLATE_CATALOG[key].tier === normalizedTier;
  });
};

