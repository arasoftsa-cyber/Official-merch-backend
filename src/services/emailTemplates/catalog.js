const freezePayloadContract = (payload = {}) =>
  Object.freeze({
    required: Object.freeze([...(payload.required || [])]),
    optional: Object.freeze([...(payload.optional || [])]),
  });

const defineEntry = ({
  tier,
  audience,
  category,
  trigger,
  implemented,
  live,
  payload,
}) =>
  Object.freeze({
    tier,
    audience,
    category,
    trigger,
    implemented: Boolean(implemented),
    live: Boolean(live),
    payload: freezePayloadContract(payload),
  });

